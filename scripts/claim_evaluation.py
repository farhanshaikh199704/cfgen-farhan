def strip_thinking_tags(text):
    """Strip <think>...</think> tags from thinking models (e.g. Qwen3.5)"""
    return re.sub(r'<think>.*?</think>', '', text, flags=re.DOTALL).strip()


def parse_is_checkable_evaluation(answer):
    """Parse LLM response for is_checkable binary evaluation"""
    # Strip thinking model artifacts before parsing
    answer = strip_thinking_tags(answer)

    reasoning = ""
    is_checkable = None

    reasoning_match = re.search(r'<REASONING>(.*?)</REASONING>', answer, re.DOTALL)
    if reasoning_match:
        reasoning = reasoning_match.group(1).strip()

    assessment_match = re.search(r'<ASSESSMENT>(.*?)</ASSESSMENT>', answer, re.DOTALL)
    if assessment_match:
        checkable_match = re.search(r'is_checkable:\s*(\d)', assessment_match.group(1))
        if checkable_match:
            is_checkable = int(checkable_match.group(1))

    if is_checkable is None:
        # Fallback: look for patterns in the full answer
        if re.search(r'\bis_checkable\s*[:=]\s*1\b', answer, re.IGNORECASE):
            is_checkable = 1
        elif re.search(r'\bis_checkable\s*[:=]\s*0\b', answer, re.IGNORECASE):
            is_checkable = 0
        else:
            raise ValueError("Could not parse is_checkable value from response")

    return reasoning, is_checkable


def parse_evaluation(answer):
    """Parse the LLM response to extract evaluation results"""
    # Default empty values
    reasoning = ""
    summary = ""
    criteria_results = {

    }
    
    # Extract reasoning
    reasoning_pattern = r'<REASONING>(.*?)</REASONING>'
    reasoning_match = re.search(reasoning_pattern, answer, re.DOTALL)
    if reasoning_match:
        reasoning = reasoning_match.group(1).strip()
    else:
        reasoning = ""
    
    # Extract evaluation
    evaluation_pattern = r'<EVALUATION>(.*?)</EVALUATION>'
    evaluation_match = re.search(evaluation_pattern, answer, re.DOTALL)
    criteria_names = [
        "self_containment", "factuality", 
        "entity_precision", "factual_alignment", "language_matching"
    ]
    if evaluation_match:
        evaluation_text = evaluation_match.group(1).strip()
        
        # Extract scores and reasoning for each criterion using the actual section names from the text
        criteria_patterns = [
            # Patterns to extract each criterion's reasoning and score
            r'1\.\s*Self-Containment:\s*\n\s*-\s*Reasoning:\s*(?:\[(.*?)\]|(.*?))\s*\n\s*-\s*Score:\s*(.*?)(?:\n\s*\n|\n$|$)',
            r'2\.\s*Factuality\s*&?\s*Verifiability:\s*\n\s*-\s*Reasoning:\s*(?:\[(.*?)\]|(.*?))\s*\n\s*-\s*Score:\s*(.*?)(?:\n\s*\n|\n$|$)',
            r'3\.\s*Entity\s*Precision:\s*\n\s*-\s*Reasoning:\s*(?:\[(.*?)\]|(.*?))\s*\n\s*-\s*Score:\s*(.*?)(?:\n\s*\n|\n$|$)',
            r'4\.\s*Factual\s*Alignment\s*\(with\s*Article\):\s*\n\s*-\s*Reasoning:\s*(?:\[(.*?)\]|(.*?))\s*\n\s*-\s*Score:\s*(.*?)(?:\n\s*\n|\n$|$)',
            r'5\.\s*Language\s*Matching:\s*\n\s*-\s*Reasoning:\s*(?:\[(.*?)\]|(.*?))\s*\n\s*-\s*Score:\s*(.*?)(?:\n\s*\n|\n$|$)'
        ]
        
        for pattern in criteria_patterns:
            match = re.search(pattern, evaluation_text, re.DOTALL)
            if match:
                criterion_name = criteria_names[criteria_patterns.index(pattern)]
                # Since we have 2 capture groups for reasoning, check which one matched
                reasoning = match.group(1) or match.group(2)
                reasoning = reasoning.strip()
                score = match.group(3).strip()
                criteria_results[f"{criterion_name}_reasoning"] = reasoning
                criteria_results[f"{criterion_name}_score"] = score
                
        
        # Extract overall quality
        quality_pattern = r'Overall Quality:\s*([^\]\n]+)'
        quality_match = re.search(quality_pattern, evaluation_text, re.DOTALL)
        if quality_match:
            quality = quality_match.group(1).strip()
            criteria_results["overall_quality"] = quality.capitalize()
    else:
        print(f"No evaluation found in the response")
        raise ValueError("No evaluation found in the response")
    
    if len(criteria_results.keys()) == 0:
        print(f"No criteria results found in the response")
        raise ValueError("No criteria results found in the response")
    elif len(criteria_results.keys()) < 5:
        print(f"Only {len(criteria_results.keys())} criteria results found in the response")
        print(f"Criteria results: {criteria_results.keys()}")
        raise ValueError("Only some criteria results found in the response")
    
    # Extract summary
    summary_pattern = r'<SUMMARY>(.*?)</SUMMARY>'
    summary_match = re.search(summary_pattern, answer, re.DOTALL)
    if summary_match:
        summary = summary_match.group(1).strip()
    else:
        summary = ""
    
    # Count yes values to determine quality level as fallback
    yes_count = sum(1 for k, v in criteria_results.items() if k.endswith('_score') and v == "Yes")
    criteria_results["overall_quality"] = "High" if yes_count == 5 else "Medium" if yes_count >= 3 else "Low"
    
    return reasoning, criteria_results, summary

