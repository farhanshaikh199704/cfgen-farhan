def main():
    parser = argparse.ArgumentParser(description='Evaluate claim quality')
    parser.add_argument('--input', type=str, required=True, help='Input CSV file with claims')
    parser.add_argument('--output', type=str, default=None, help='Output file path')
    parser.add_argument('--port', type=int, default=7501, help='Port for the SGLang server')
    parser.add_argument('--max_workers', type=int, default=100,
                        help='Maximum number of concurrent worker threads')
    parser.add_argument('--batch_size', type=int, default=50,
                        help='Number of claims to process in each batch')
    parser.add_argument('--retry_attempts', type=int, default=3,
                        help='Number of retry attempts for failed requests')
    parser.add_argument('--model', type=str, default="deepseek-ai/DeepSeek-V3",
                        help='Model to use for evaluation')
    parser.add_argument('--tp', type=int, default=8,
                        help='Number of GPUs to use for tensor parallelism')
    parser.add_argument('--trust-remote-code', action='store_true', default=True,
                        help='Trust remote code for the model')
    parser.add_argument('--sample', type=int, default=None,
                        help='Sample size to evaluate (default: evaluate all claims)')
    parser.add_argument('--mode', type=str, default='quality', choices=['quality', 'is_checkable'],
                        help='Evaluation mode: quality (5-criteria rubric) or is_checkable (binary)')
    args = parser.parse_args()

    # Load claims data
    print(f"Loading claims from {args.input}...")
    claims_df = pd.read_csv(args.input)

    if args.mode == 'quality':
        # rename source_article to article_text and synthetic_claim to claim
        claims_df = claims_df.rename(columns={'source_article': 'article_text', 'synthetic_claim': 'claim'})

    # Sanity check: ensure 'claim' column exists
    if 'claim' not in claims_df.columns:
        print(f"Error: 'claim' column not found. Available columns: {claims_df.columns.tolist()}")
        return

    print(f"In the input file, there are {len(claims_df)} claims")
    # Sample if requested
    if args.sample and args.sample < len(claims_df):
        print(f"Sampling {args.sample} claims for evaluation...")
        claims_df = claims_df.sample(args.sample, random_state=42)

    # Initialize claim evaluator
    evaluator = ClaimEvaluator(
        model_name=args.model,
        port=args.port,
        max_workers=args.max_workers,
        retry_attempts=args.retry_attempts,
        tensor_parallel=args.tp
    )

    # Start the server
    print(f"Starting SGLang server for {args.model} with tensor parallelism {args.tp}...")
    if not evaluator.start_server():
        print("Failed to start SGLang server. Exiting.")
        return

    try:
        start_time = time.time()
        timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
        input_filename = os.path.basename(args.input).replace('.csv', '')

        if args.mode == 'is_checkable':
            # --- is_checkable binary evaluation (claim-only, no article) ---
            print(f"Starting is_checkable evaluation with {args.max_workers} workers...")
            results_df = evaluator.process_claims_is_checkable(
                claims_df=claims_df,
                batch_size=args.batch_size
            )

            total_time = time.time() - start_time
            print(f"Completed: {evaluator.successful_count}/{evaluator.claim_count} claims in {total_time/60:.1f}m")
            print(f"Success rate: {100 * evaluator.successful_count / evaluator.claim_count:.1f}%")

            # Summary stats
            valid = results_df[results_df['is_checkable_llm'] >= 0]
            print(f"\nis_checkable distribution (valid={len(valid)}):")
            print(f"  Checkable (1): {(valid['is_checkable_llm'] == 1).sum()}")
            print(f"  Not checkable (0): {(valid['is_checkable_llm'] == 0).sum()}")
            if (results_df['is_checkable_llm'] == -1).any():
                print(f"  Failed (-1): {(results_df['is_checkable_llm'] == -1).sum()}")

            # Save results
            output_file = args.output if args.output else f"is_checkable_{input_filename}_{timestamp}.csv"
            results_df.to_csv(output_file, index=False)
            print(f"\nResults saved to {output_file}")

        else:
            # --- Existing 5-criteria quality evaluation ---
            print(f"Starting claim evaluation with {args.max_workers} workers...")
            results_df = evaluator.process_claims(
                claims_df=claims_df,
                batch_size=args.batch_size
            )

            total_time = time.time() - start_time
            print(f"Completed: {evaluator.successful_count}/{evaluator.claim_count} claims in {total_time/60:.1f}m")
            print(f"Success rate: {100 * evaluator.successful_count / evaluator.claim_count:.1f}%")

            stats = calculate_statistics(results_df)

            print("\nQuality Distribution:")
            for quality, data in stats['quality_distribution'].items():
                percentage = stats['quality_percentages'][quality]
                print(f"  {quality}: {data} ({percentage:.1f}%)")

            print("\nCriteria Pass Rates:")
            for criterion, data in stats['criteria_pass_rates'].items():
                print(f"  {criterion}: {data:.1f}%")

            output_stats_path = f"stats/{input_filename}_stats_{timestamp}.json"

            def convert_numpy_types(obj):
                if isinstance(obj, dict):
                    return {k: convert_numpy_types(v) for k, v in obj.items()}
                elif isinstance(obj, list):
                    return [convert_numpy_types(v) for v in obj]
                elif isinstance(obj, tuple):
                    return tuple(convert_numpy_types(v) for v in obj)
                elif hasattr(obj, 'item'):
                    return obj.item()
                else:
                    return obj

            stats = convert_numpy_types(stats)
            with open(output_stats_path, 'w') as f:
                json.dump(stats, f, indent=2)
            print(f"\nDetailed statistics saved to {output_stats_path}")

            tracking_file = update_stats_csv(args.input, stats)
            print(f"Statistics added to tracking file: {tracking_file}")

            output_file = args.output if args.output else f"evaluated_{input_filename}_{timestamp}.csv"
            eval_columns = [
                'self_containment_reasoning', 'self_containment_score', 'factuality_reasoning', 'factuality_score',
                'entity_precision_reasoning', 'entity_precision_score', 'factual_alignment_reasoning', 'factual_alignment_score',
                'language_matching_reasoning', 'language_matching_score', 'overall_quality'
            ]
            ordered_columns = [col for col in claims_df.columns if col not in eval_columns + ['reasoning', 'summary']]
            ordered_columns += eval_columns + ['reasoning', 'summary']
            existing_columns = [col for col in ordered_columns if col in results_df.columns]
            results_df[existing_columns].to_csv(output_file, index=False)
            print(f"Results saved to {output_file}")

    finally:
        evaluator.cleanup()

